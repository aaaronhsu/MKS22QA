# append
a = ["hello"]
a.append("sir")
print(a)

# clear
l = ["This", "list", "contains", "things"]
a.clear()
print(a)

# copy
l = ["potato"]
a = l.copy()
print(a)

# count
l = [1, 2, 3, 4, 5, 2, 3, 2]
a = l.count(2)
print(a)

# extend
l = ["I"]
l.extend(["want", "to", "sleep"])
print(l)

# max
l = [1, 4, 2, 6, 2, 6, 8, 3]
max = max(l)
print(max)

# min
l = [1, 4, 2, 6, 2, 6, 8, 3]
min = min(l)
print(min)

# index
l = [1, 5, 2, 3]
index = l.index(5)
print(index)

# insert
l = [1, 2, 4, 5]
l.insert(2, 3)
print(l)

# pop
l = [4, 3, 2, 2, 1]
l.pop(2)
print(l)

# remove
l = [1, 2, 3, 3, 4, 5]
